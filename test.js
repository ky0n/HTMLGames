if ((typeof(gnuplot.active_plot) == "undefined" || gnuplot.active_plot != test)  &&  typeof(gnuplot.mouse_update) != "undefined") {
  gnuplot.active_plot_name = "test";
  gnuplot.active_plot = test;
  canvas.onmousemove = gnuplot.mouse_update;
  canvas.onmouseup = gnuplot.zoom_in;
  canvas.onmousedown = gnuplot.saveclick;
  canvas.onkeypress = gnuplot.do_hotkey;
  if (canvas.attachEvent) {canvas.attachEvent('mouseover', test);}
  else if (canvas.addEventListener) {canvas.addEventListener('mouseover', test, false);} 
  gnuplot.zoomed = false;
  gnuplot.zoom_axis_width = 0;
  gnuplot.zoom_in_progress = false;
  gnuplot.polar_mode = false;
  gnuplot.polar_theta0 = 0;
  gnuplot.polar_sense = 1;
  ctx.clearRect(0,0,600,400);
}
